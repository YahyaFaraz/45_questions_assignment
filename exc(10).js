// Storing variable with whitespaces on bothsides by applying \t\n on both sides
var name1 = "\t\n Yahya \t\n";
console.log("With whitespaces:" + name1);
// Using builtin function named trim to remove whitespaces from the string
console.log("Without whitespaces:" + "\n" + name1.trim());
